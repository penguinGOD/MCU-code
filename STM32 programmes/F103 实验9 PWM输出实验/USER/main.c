#include "led.h"
#include "delay.h"
#include "key.h"
#include "sys.h"
#include "usart.h"
#include "timer.h"

 int main(void)
 {		
 	u16 led0pwmval=4800;
	delay_init();	    	 //延时函数初始化	  
	NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2); 	 //设置NVIC中断分组2:2位抢占优先级，2位响应优先级
	uart_init(115200);	 //串口初始化为115200
 	LED_Init();			     //LED端口初始化
 	TIM3_PWM_Init(4999,287);	 //不分频。PWM频率=72000000/（arr+1）(psc+1)=50HZ
   	while(1)
	{
 		delay_ms(100);	 
    led0pwmval--;
    if(led0pwmval<4600) led0pwmval=4600;								 
		TIM_SetCompare2(TIM3,led0pwmval);  //   led0pwmval/arr=1-占空比
	}	 
 }

